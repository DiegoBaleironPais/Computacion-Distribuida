    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (GestionVentanas.esCadenaVacia(jTextField1.getText())){
            jLabel4.setVisible(true);
        } else {
            String mensaje;
            jLabel4.setVisible(false);
            DatagramPacket mensajeDeSalida;
            byte[] bufferSalida = new byte[1000];
            mensaje = userNickName + ": " + jTextField1.getText();
            bufferSalida = mensaje.getBytes();
            mensajeDeSalida = new DatagramPacket(bufferSalida, bufferSalida.length,
                Socket.getGroup(), Socket.getSocket().getLocalPort());
            jTextField1.setText("");
            try {
                Socket.getSocket().send(mensajeDeSalida);
            } catch (IOException ex) {
                Logger.getLogger(VentanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }    
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

